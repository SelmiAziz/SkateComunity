package beans;


public class CustomBoardBean {
    private String name;
    private int extraPiles;
    private boolean useExtraPiles;
    private double gripTexture;
    private boolean useGripTexture;
    private double concaveNose;
    private boolean useConcaveNose;
    private double concaveTail;
    private boolean useConcaveTail;
    private int warrantyMonths;
    private boolean useWarrantyMonths;

    public CustomBoardBean() {}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getExtraPiles() {
        return extraPiles;
    }

    public void setExtraPiles(int extraPiles) {
        this.extraPiles = extraPiles;
    }

    public boolean getUseExtraPiles() {
        return useExtraPiles;
    }

    public void setUseExtraPiles(boolean useExtraPiles) {
        this.useExtraPiles = useExtraPiles;
    }

    public double getGripTexture() {
        return gripTexture;
    }

    public void setGripTexture(double gripTexture) {
        this.gripTexture = gripTexture;
    }

    public boolean getUseGripTexture() {
        return useGripTexture;
    }

    public void setUseGripTexture(boolean useGripTexture) {
        this.useGripTexture = useGripTexture;
    }

    public double getConcaveNose() {
        return concaveNose;
    }

    public void setConcaveNose(double concaveNose) {
        this.concaveNose = concaveNose;
    }

    public boolean getUseConcaveNose() {
        return useConcaveNose;
    }

    public void setUseConcaveNose(boolean useConcaveNose) {
        this.useConcaveNose = useConcaveNose;
    }

    public double getConcaveTail() {
        return concaveTail;
    }

    public void setConcaveTail(double concaveTail) {
        this.concaveTail = concaveTail;
    }

    public boolean getUseConcaveTail() {
        return useConcaveTail;
    }

    public void setUseConcaveTail(boolean useConcaveTail) {
        this.useConcaveTail = useConcaveTail;
    }

    public int getWarrantyMonths() {
        return warrantyMonths;
    }

    public void setWarrantyMonths(int warrantyMonths) {
        this.warrantyMonths = warrantyMonths;
    }

    public boolean getUseWarrantyMonths() {
        return useWarrantyMonths;
    }

    public void setUseWarrantyMonths(boolean useWarrantyMonths) {
        this.useWarrantyMonths = useWarrantyMonths;
    }
}
